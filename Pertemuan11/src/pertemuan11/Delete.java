    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("pertemuan11PU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            // PK = absen (typo kalau string â†’ harus diubah ke int)
            Datasiswa m = em.find(Datasiswa.class, Integer.parseInt(absenLama));

            if (m != null) {
                em.remove(m);
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                this.dispose(); // tutup dialog setelah delete
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
                em.getTransaction().rollback();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            em.close();
            emf.close();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

